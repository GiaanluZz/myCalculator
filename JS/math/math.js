function somma(a, b) {

    return a + b;
}

function sottrazione(a, b) {

    return a - b;
}

function moltiplicazione(a, b) {

    return a * b;
}

function divisione(a, b) {

    return a / b;
}

export{somma, sottrazione, moltiplicazione, divisione};
